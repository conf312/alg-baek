package leetcode.problems;
/*
이 문제는 "Nim 게임"으로 잘 알려져 있으며, 최적의 전략을 통해 누가 승리할 수 있는지 알아내는 문제입니다. 핵심은 주어진 돌의 개수 n에 대해 최적의 플레이를 가정했을 때, 첫 번째 플레이어가 승리할 수 있는지를 판단하는 것입니다.

해결 방법
Nim 게임의 규칙에 따르면, 만약 돌의 개수가 4의 배수라면, 최적의 플레이어는 항상 패배합니다. 이는 상대방에게 돌의 개수를 항상 4의 배수로 남겨주면, 상대방은 이길 수 있는 최적의 플레이를 할 수 있기 때문입니다. 예를 들어:

돌의 개수가 4개라면, 어떤 경우에도 첫 번째 플레이어가 마지막 돌을 가져가게 되는 상황이 발생합니다.
돌의 개수가 5, 6, 7개라면, 첫 번째 플레이어는 각각 1, 2, 3개의 돌을 가져가서 상대방에게 4개의 돌을 남길 수 있습니다. 이 경우 상대방은 패배하게 됩니다.
따라서, 돌의 개수가 4의 배수가 아니라면 첫 번째 플레이어가 항상 이길 수 있습니다. 이를 일반화하면, 돌의 개수가 4의 배수인 경우에만 첫 번째 플레이어는 패배합니다.
*/
class CanWinNim {
  public boolean canWinNim(int n) {
      return n % 4 != 0;
  }
}
